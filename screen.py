FALLBACK = (0x00, 0x3C, 0x42, 0x04, 0x08, 0x00, 0x08, 0x00)

CHAR_W = 8  # largura em pixels de 1 caractere (escala 1)
CHAR_H = 8  # altura em pixels de 1 caractere (escala 1)


def _get_bitmap(ch):
    return FONT.get(ch.upper(), FONT.get(ch, FALLBACK))


# ═══ Funções de desenho ═══════════════════════════════════════════

def draw_char(d, x, y, ch, scale, r, g, b):
    """
    Desenha UM caractere na posição (x,y) com escala e cor.
    Não desenha fundo — só os pixels 'ligados' da fonte.
    """
    bitmap = _get_bitmap(ch)
    for row in range(8):
        bits = bitmap[row]
        for col in range(8):
            if bits & (0x80 >> col):
                d.fill_rect(x + col * scale, y + row * scale,
                            scale, scale, r, g, b)


def draw_text(d, text, x, y, scale, r, g, b, max_w=None):
    """
    Desenha uma string na posição (x,y).
    Suporta \n para quebra de linha.
    Retorna (prox_x, prox_y) para continuar depois.
    """
    cw = CHAR_W * scale
    ch = CHAR_H * scale
    cx, cy = x, y

    for ch_char in text:
        if ch_char == '\n':
            cx = x
            cy += ch + 2
            continue
        if max_w and (cx + cw > x + max_w):
            cx = x
            cy += ch + 2
        draw_char(d, cx, cy, ch_char, scale, r, g, b)
        cx += cw

    return cx, cy


def draw_text_center(d, text, y, scale, r, g, b):
    """Desenha texto centralizado horizontalmente. Quebra em \n."""
    cw = CHAR_W * scale
    ch = CHAR_H * scale + 2
    lines = text.split('\n')
    for line in lines:
        tw = len(line) * cw
        x = (d.width - tw) // 2
        draw_text(d, line, x, y, scale, r, g, b)
        y += ch


def draw_ascii_art(d, art, x, y, scale, color, bg_color=None):
    """
    Desenha arte ASCII linha por linha.
    Cada caractere diferente de espaço é desenhado na cor `color`.
    Se bg_color for fornecido, pinta o fundo da área da arte primeiro.
    Retorna (largura_total, altura_total) em pixels.
    """
    lines = art.split('\n')
    art_w = max(len(l) for l in lines)
    art_h = len(lines)
    pw = art_w * CHAR_W * scale
    ph = art_h * CHAR_H * scale

    # Fundo opcional
    if bg_color:
        d.fill_rect(x, y, pw, ph, bg_color[0], bg_color[1], bg_color[2])

    for li, line in enumerate(lines):
        for ci, ch in enumerate(line):
            if ch != ' ':
                draw_char(d, x + ci * CHAR_W * scale,
                          y + li * CHAR_H * scale,
                          ch, scale, color[0], color[1], color[2])

    return pw, ph


